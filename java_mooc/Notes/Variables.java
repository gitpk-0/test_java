// Variables

// A variable can be thought of as a container in which information of a given 
// type can be stored. Examples of these different types include text (String), 
// whole numbers (int), floating-point numbers (double), and whether something 
// is true or false (boolean). A value is assigned to a variable using 
// the equals sign (=).

// A variable exists from the moment of its declaration, and its initial value 
// is preserved until another value is assigned to it. You can change a 
// variable's value using a statement that comprises the variable name,
// an equals sign, and the new value to be assigned. Remember to keep in 
// mind that the variable type is only stated during the 
// initial variable declaration.

// Once a variable's type has been declared, it can no longer be changed. 
// For example, a boolean value cannot be assigned to a variable of the 
// integer type, nor can an integer be assigned to a variable of 
// the boolean type.

// However, exceptions do exist: an integer can be assigned to a variable of 
// the double type, since Java knows how to convert an integer to 
// a double during assignment.

// A floating-point value cannot, however, be assigned to an integer variable. 
// The reason for this is that those who develop the language aim to 
// prevent developers from making errors that lead to a loss of information.